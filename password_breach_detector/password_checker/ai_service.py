logger = logging.getLogger(__name__)

class BaseAIProvider:
    """Base interface for all AI provider engines (Gemini, OpenAI, Local, etc.)"""
    def generate_response(self, system_instruction, user_prompt, context):
        raise NotImplementedError("Providers must implement generate_response")

class GeminiProvider(BaseAIProvider):
    """Google Gemini API Provider implementation using raw HTTP requests"""
    
    def generate_response(self, system_instruction, user_prompt, context):
        api_key = getattr(settings, 'GEMINI_API_KEY', None)
        if not api_key:
            raise ValueError("Gemini API key is not configured.")
        
        # Standard endpoint for Gemini 1.5 Flash
        url = f"https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key={api_key}"
        
        headers = {
            'Content-Type': 'application/json'
        }
        
        # Combine system instruction, context summary, and user question
        full_prompt = f"{system_instruction}\n\n[USER CONTEXT]\n{json.dumps(context, indent=2)}\n\nUser Question: {user_prompt}"
        
        payload = {
            "contents": [{
                "parts": [{"text": full_prompt}]
            }]
        }
        
        try:
            response = requests.post(url, headers=headers, json=payload, timeout=8)
            if response.status_code == 200:
                res_data = response.json()
                # Parse response text
                text = res_data['candidates'][0]['content']['parts'][0]['text']
                return text, False # (response text, is_fallback)
            else:
                logger.error(f"Gemini API returned status code {response.status_code}: {response.text}")
                raise Exception("Gemini API call failed")
        except Exception as e:
            logger.error(f"Error calling Gemini API: {str(e)}")
            raise e

