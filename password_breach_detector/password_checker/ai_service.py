class ContextBuilder:
    """Assembles localized user statistics, audit logs, and status records"""
    
    @staticmethod
    def build_user_context(user):
        if not user or user.is_anonymous:
            return {
                "user_authenticated": False,
                "security_score": 0,
                "total_checks": 0,
                "has_active_2fa": False,
                "compromised_count": 0,
                "reused_count": 0,
                "recent_checks": []
            }
            
        # Get checks
        from password_checker.models import PasswordCheck
        checks = PasswordCheck.objects.filter(user=user)
        total_checks = checks.count()
        compromised = checks.filter(was_breached=True).count()
        
        # Reuse calculation
        reused_groups = checks.values('sha256_hash').annotate(count=Count('id')).filter(count__gt=1)
        reused_count = sum(group['count'] for group in reused_groups) if reused_groups else 0
        
        # Check 2FA
        has_2fa = user.use_two_factor or user.has_active_2fa
        
        # Calculate score dynamically
        score = 100
        if not has_2fa:
            score -= 20
        if total_checks == 0:
            score -= 20
        else:
            if compromised > 0:
                score -= (compromised * 15)
            if reused_count > 0:
                score -= (reused_count * 10)
        score = max(0, min(100, score))
        
        # Recent checks
        recent_list = []
        for c in checks[:5]:
            recent_list.append({
                "hash_prefix": c.hash_prefix,
                "was_breached": c.was_breached,
                "breach_count": c.breach_count,
                "strength_score": float(c.strength_score) if c.strength_score else 0.0,
                "check_timestamp": c.check_timestamp.strftime("%Y-%m-%d %H:%M")
            })
            
        return {
            "user_authenticated": True,
            "username": user.username,
            "security_score": score,
            "total_checks": total_checks,
            "has_active_2fa": has_2fa,
            "compromised_count": compromised,
            "reused_count": reused_count,
            "recent_checks": recent_list
        }

class PromptBuilder:
    """Builds system instructions and prompt context"""
    
    @staticmethod
    def get_system_instruction():
        return (
            "You are 'Sentinel AI', a friendly, highly professional cybersecurity assistant integrated "
            "into the Password Security Platform. Your job is to answer the user's questions about security, "
            "password auditing, and data breaches.\n\n"
            "CRITICAL SECURITY GUIDELINES:\n"
            "1. NEVER ask for, show, or discuss plain-text passwords.\n"
            "2. NEVER reveal sensitive backend server keys or file paths.\n"
            "3. Refer directly to the user's personal context data provided below to give personalized advice.\n"
            "4. Format your responses clearly using Markdown headings, bullet points, and bold text."
        )

class AIService:
    """AI Service router selecting providers dynamically with seamless fallback"""
    
    @staticmethod
    def chat(user, user_prompt):
        context = ContextBuilder.build_user_context(user)
        system_instruction = PromptBuilder.get_system_instruction()
        
        api_key = getattr(settings, 'GEMINI_API_KEY', None)
        
        # If API key exists, try Gemini Provider
        if api_key:
            try:
                provider = GeminiProvider()
                return provider.generate_response(system_instruction, user_prompt, context)
            except Exception as e:
                logger.warning(f"GeminiProvider failed, falling back to local advisor: {str(e)}")
        
        # Fall back to local rule-based expert advisor
        provider = FallbackProvider()
        return provider.generate_response(system_instruction, user_prompt, context)
